"""
Module: draw_pentagon

Program to draw a pentagon using turtles.
"""

import turtle

# create a turtle and set the pen color
sat = turtle.Turtle()
sat.pencolor("red")

# draw the pentagon
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)


